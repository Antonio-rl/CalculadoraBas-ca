public class Operaciones {
    
   public double total;
   
   //Metodos
   
   public void suma(double a, double b){
       total = a + b;
   }
   
   public void resta(double a, double b){
       total = a - b;
   }
   
   public void multiplica(double a, double b){
       total = a * b;
   }
   
   public void divide(double a, double b){
       total= a / b;
   }
    
}
